export const matchesCategory = (car, category) => {
  if (category === "All Cars") return true;
  if (category === "Electric") return car.fuel === "Electric" || car.bodyType === "Electric";
  if (category === "Luxury") return (car.priceRaw || 0) >= 10000000; // Above 1 Crore
  if (category === "Sports") {
    const v = (car.variant || "").toLowerCase();
    const b = (car.bodyType || "").toLowerCase();
    return b === "sports" || b === "coupe" || v.includes("amg") || v.includes("gt") || v.includes("m sport") || v.includes("rs");
  }
  return car.bodyType === category;
};

